#15- Operadores de asignacion , me permite dar un valor a una variable
edad = 20
print(edad)
print("-----------------------------")

# += , se le suma n cantidad al valor ya dado

edad += 5
print(edad)
print("-----------------------------")

#-=, se le resta n cantidad al valor ya dado
edad -=5
print(edad)
print("-----------------------------")

